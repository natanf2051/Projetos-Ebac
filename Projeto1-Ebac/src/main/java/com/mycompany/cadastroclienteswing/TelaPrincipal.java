    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
       String nome = BoxName.getText();
       String Cpf = BoxCPF.getText();
       String Endereco = BoxEndereco.getText();
       String Numero = BoxNumero.getText();
       String Cidade = BoxCidade.getText();
       String Estado = BoxEstado.getText();
       String Telefone = BoxTelefone.getText();
       
       if(!IsValida(nome, Cpf, Endereco, Numero, Cidade, Estado, Telefone)){
          JOptionPane.showMessageDialog(null, "Preencha todos os campos",
         "Resultado", JOptionPane.INFORMATION_MESSAGE);
          return;
      }
       
       Clientes cliente = new Clientes(nome, Cpf, Endereco, Numero, Cidade, Estado, Telefone);
       Boolean isCadastrado = this.ClienteDAO.cadastrar(cliente);
       if(isCadastrado){
           modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getEndereco(), cliente.getNumero(), cliente.getCidade(), cliente.getEstado(), cliente.getTelefone()});
           limparcampos();
       }else{
       JOptionPane.showMessageDialog(null, "Cliente ja cadastrado.",
         "ATENÇÃO!", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_SalvarActionPerformed

