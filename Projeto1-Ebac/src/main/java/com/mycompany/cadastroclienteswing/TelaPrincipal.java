    private void TabelaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaClientesMouseClicked
            int result = TabelaClientes.getSelectedRow();
            Long Cpf = (Long) TabelaClientes.getValueAt(result, 1);
            
            Clientes cliente = this.ClienteDAO.consultar(Cpf);
            BoxName.setText(cliente.getNome());
            BoxCPF.setText(cliente.getCpf().toString());
            BoxEndereco.setText(cliente.getEndereco());
            BoxNumero.setText(cliente.getNumero().toString());
            BoxCidade.setText(cliente.getCidade());
            BoxEstado.setText(cliente.getEstado());
            BoxTelefone.setText(cliente.getTelefone().toString());
            
    }//GEN-LAST:event_TabelaClientesMouseClicked

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
         int linha = TabelaClientes.getSelectedRow();
            if(linha >= 0){
              int result = JOptionPane.showConfirmDialog(this,"Deseja excluir essa pessoa", "AVISO", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
                
              if(result == JOptionPane.YES_OPTION){
                    Long Cpf = (Long) TabelaClientes.getValueAt(linha, 1);
                    this.ClienteDAO.excluir(Cpf);
                    modelo.removeRow(linha);
                    
                    JOptionPane.showMessageDialog(null, "Cliente excluido com sucesso.",
         "AVISO!", JOptionPane.INFORMATION_MESSAGE);
                    
                }
            }else{
                JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado.",
         "AVISO!", JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_ExcluirActionPerformed

