    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
       int linhaSelecionada = TabelaClientes.getSelectedRow();
        
       if(linhaSelecionada >= 0){
       String nome = BoxName.getText();
       String Cpf = BoxCPF.getText();
       String Endereco = BoxEndereco.getText();
       String Numero = BoxNumero.getText();
       String Cidade = BoxCidade.getText();
       String Estado = BoxEstado.getText();
       String Telefone = BoxTelefone.getText();
       
       if(!IsValida(nome, Cpf, Endereco, Numero, Cidade, Estado, Telefone)){
          JOptionPane.showMessageDialog(null, "Preencha todos os campos",
         "Resultado", JOptionPane.INFORMATION_MESSAGE);
          return;
      }
       
      
       Clientes clientenovo = new Clientes(nome, Cpf, Endereco, Numero, Cidade, Estado, Telefone);
       this.ClienteDAO.alterar(clientenovo);
       
       modelo.removeRow(linhaSelecionada);
       modelo.addRow(new Object[]{clientenovo.getNome(), clientenovo.getCpf(), clientenovo.getEndereco(), clientenovo.getNumero(), clientenovo.getCidade(), clientenovo.getEstado(), clientenovo.getTelefone()});
       
       
       JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso.",
         "Resultado", JOptionPane.INFORMATION_MESSAGE);
          limparcampos();
       }else{
           JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado",
         "Resultado", JOptionPane.INFORMATION_MESSAGE);
         
       }
       
    }//GEN-LAST:event_AlterarActionPerformed

